def PublicWordGen(secret_word):
        if '-' in secret_word:
            public_word = ['_' if i != '-' else '-' for i in secret_word]

        else:
            public_word = ['_' if i != ' ' else ' ' for i in secret_word]
        
        return ''.join(public_word)

